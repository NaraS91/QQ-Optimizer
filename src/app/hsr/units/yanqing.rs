pub fn modifiers(unit: &Unit) -> Vec<ModifierOrDOT> {
    vec![ModifierOrDOT::Modifier(Modifier::new(
        (unit.kind, Source::Trace(3)),
        vec![ModifierData::new(
            ModifierTarget::Caster,
            Stat::Base(BaseStat::Spd),
            BuffScaling::Multiplicative,
            flat_value!(0.1),
        )],
        true,
    ))]
}

